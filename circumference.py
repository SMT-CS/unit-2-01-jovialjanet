import math


def main() -> None:
    """
    The main() function calculates the circumference of a 7 mm diameter circle,
        returns None.
    """
    print("If a circle has a diameter of 7 mm: ")
    print("")
    print(f"Circumference is {(math.pi * 7):.2f} mm.")

    print("\nDone.")


if __name__ == "__main__":
    main()
